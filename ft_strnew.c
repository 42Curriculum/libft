#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	i = 0;
	if (!(str = (char *)malloc((size + 1) * sizeof(*str))))
		return (NULL);
	ft_memset(str, '\0', size);
	return (str);
}
